    private void tablaclienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaclienteMouseClicked
        int fila = tablacliente.getSelectedRow();
        int id = Integer.parseInt(tablacliente.getValueAt(fila, 0).toString());
        Cliente cl = clienteImp.ConsultarCliente(id);
        
        JtextNombre.setText(cl.getNombre());
        JtextApp.setText(cl.getApellido_paterno());
        JtextColonia1.setText(cl.getColonia());
        JtextCP.setText(cl.getCp());
        JtextCalle.setText(cl.getCalle());
        JtextCiudad.setText(cl.getCiudad());
        JtextRfc.setText(cl.getRfc());
        JtextTel.setText(cl.getTelefono());
        JtextCP.setText(cl.getCp());
    }//GEN-LAST:event_tablaclienteMouseClicked

