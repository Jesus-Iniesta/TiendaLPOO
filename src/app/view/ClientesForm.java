    public void cargarTabla() {
        DefaultTableModel modeloTabla = (DefaultTableModel) tablacliente.getModel();
        modeloTabla.setRowCount(0);

        int[] anchos = {2, 70, 70, 70, 8, 8, 80, 50, 50, 50};
        for (int i = 0; i < tablacliente.getColumnCount(); i++) {
            tablacliente.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        clienteImp.construirTabla(modeloTabla);

    }
    
