    private void tablaventaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaventaMouseClicked
        int fila = tablaventa.getSelectedRow();
        int id = Integer.parseInt(tablaventa.getValueAt(fila, 0).toString());
        DetalleVenta detalles = detalle.ConsultarDetalle(id);
        
        jComboProducto.setSelectedItem(detalle.consultarProducto(detalles.getId_producto()));
        jComboCliente.setSelectedItem(detalle.consultarCliente(detalles.getId_venta()));
        
        jCantidad.setValue(detalles.getCantidad());
        jTextSubtotal.setText(detalles.getSubtotal()+"");
        jTextTotal.setText(detalles.getSubtotal()+"");
        jTextPrecio.setText(detalles.getPrecio_unitario()+"");
    }//GEN-LAST:event_tablaventaMouseClicked

