    public void llenarProductos(){
        ArrayList<String> productos = pedidoImp.ProductosList();
        
        for (int i = 0; i < productos.size(); i++) {
            jComboProducto.addItem(productos.get(i));
        }
    }
    
    public void llenarClientes(){
       ArrayList<String> prueba = venta.ListaClientes();
       jComboCliente.addItem("Vacio");
       for(int i = 0; i < prueba.size();i++){
           jComboCliente.addItem(prueba.get(i));
           System.out.println("Clientes: "+prueba.get(i));
       }
    }
    
    public void actualizarSubtotal(){
        int cantidad = (int) jCantidad.getValue();
        double precio_unitario = Double.parseDouble(jTextPrecio.getText());
        jTextSubtotal.setText(cantidad*precio_unitario+"");
        jTextTotal.setText(jTextSubtotal.getText());
    }
    
    public void limpiar(){
        jCantidad.setValue(1);
        jComboCliente.setSelectedIndex(0);
        jComboProducto.setSelectedIndex(0);
    }
    
    

