    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String clientes = jComboCliente.getSelectedItem().toString();
        String[] id_clientes = clientes.split(" ");
        String productos = jComboProducto.getSelectedItem().toString();
        String[] id_producto = productos.split(" ");
        int id_cliente = jComboCliente.getSelectedItem().equals("Vacio") ? 0:Integer.parseInt(id_clientes[0]);
        int id_venta = 0;
        Ventas nuevaVenta = new Ventas(id_cliente, Double.parseDouble(jTextTotal.getText()));
        DetalleVenta nuevosDetalles = new DetalleVenta(Integer.parseInt(id_producto[0]), (int) jCantidad.getValue(), Double.parseDouble(jTextPrecio.getText()));
        
        venta.GuardarVenta(nuevaVenta, nuevosDetalles);
        cargarTabla();
        limpiar();

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String clientes = jComboCliente.getSelectedItem().toString();
        String[] id_clientes = clientes.split(" ");
        String productos = jComboProducto.getSelectedItem().toString();
        String[] id_producto = productos.split(" ");
        int id_cliente = jComboCliente.getSelectedItem().equals("Vacio") ? 0:Integer.parseInt(id_clientes[0]);
        int fila = tablaventa.getSelectedRow();
        int id_venta = Integer.parseInt(tablaventa.getValueAt(fila, 1).toString());
        int id_detalle = Integer.parseInt(tablaventa.getValueAt(fila, 0).toString());
       
        Ventas nuevaVenta = new Ventas(id_cliente, Double.parseDouble(jTextTotal.getText()));
        DetalleVenta nuevosDetalles = new DetalleVenta(Integer.parseInt(id_producto[0]), (int) jCantidad.getValue(), Double.parseDouble(jTextPrecio.getText()));
        
        
        venta.ModificarVentas(nuevaVenta, nuevosDetalles, id_venta, id_detalle);
        cargarTabla();
        limpiar();
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tablaventa.getSelectedRow();
        int id_venta = Integer.parseInt(tablaventa.getValueAt(fila, 1).toString());
        venta.EliminarVenta(id_venta);
        cargarTabla();
        limpiar();
                
    }//GEN-LAST:event_btnEliminarActionPerformed

